package irille.view.usr;

import irille.view.BaseView;

public class AccountSettingsView implements BaseView {
	private String productionCapacity;// 生产能力
	private String totalProduction;//生产总数
	private String numberEmployees;//员工总数
	private String annualSalesFigure;//年销售量
	private String thirdMarket;//第三市场
	private String materialSelection;//材料选择
	private String avatar;//头像
	private String logo;//LOGO
	private String department;//部门
	private String jobTitle;//职称
	private String website;//网站
	public String getProductionCapacity() {
		return productionCapacity;
	}
	public void setProductionCapacity(String productionCapacity) {
		this.productionCapacity = productionCapacity;
	}
	public String getTotalProduction() {
		return totalProduction;
	}
	public void setTotalProduction(String totalProduction) {
		this.totalProduction = totalProduction;
	}
	public String getNumberEmployees() {
		return numberEmployees;
	}
	public void setNumberEmployees(String numberEmployees) {
		this.numberEmployees = numberEmployees;
	}
	public String getAnnualSalesFigure() {
		return annualSalesFigure;
	}
	public void setAnnualSalesFigure(String annualSalesFigure) {
		this.annualSalesFigure = annualSalesFigure;
	}
	public String getThirdMarket() {
		return thirdMarket;
	}
	public void setThirdMarket(String thirdMarket) {
		this.thirdMarket = thirdMarket;
	}
	public String getMaterialSelection() {
		return materialSelection;
	}
	public void setMaterialSelection(String materialSelection) {
		this.materialSelection = materialSelection;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	
	
}
